package firstpackage;

public class SwitchCaseExample4 {

	public static void main(String[] args) {

		int monthNo = 5;

		switch(monthNo) {
		case 1:
			System.out.println("I am Jan");
			
		case 2:
			System.out.println("I am Feb");
			
		case 3:
			System.out.println("I am Mar");
			
		case 4:
			System.out.println("I am Apr");
			
		case 5:
			System.out.println("I am May");
			
		case 6:
			System.out.println("I am Jun");
			
		case 7:
			System.out.println("I am Jul");
			
		case 8:
			System.out.println("I am Aug");
			
		case 9:
			System.out.println("I am Sep");
			
		case 10:
			System.out.println("I am Oct");
			
		case 11:
			System.out.println("I am Nov");
			
		case 12:
			System.out.println("I am Dec");
			
		default:
			System.out.println("Wrong Input");
			
		}

	}

}
