package firstpackage;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		int i = 4;
		
		while( i < 3) {
			
			System.out.println("While loop example");
			i++;
		}
		
		
		System.out.println("--------------------");
		int j = 4;
		do {
		
			System.out.println("Do While loop example");
		
			j++;
			
		} while(j<3);

		
	}

}
