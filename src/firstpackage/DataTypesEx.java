package firstpackage;

public class DataTypesEx {

	public static void main(String[] args) {
		
		byte b = 20;
		short s = 500;
		int i = 4000;
		long l = 50000;
		
		System.out.println(l);
		
		float f = 30.57534f;
		
		double d = 778.46466465;
		
		System.out.println(d);
		
		char c = 'a';
		System.out.println(c);
		
		boolean bool = false;
		System.out.println(bool);
		
		

	}

}
